import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

# Load the dataset
df = pd.read_csv('/content/traffic_accidents.csv')

# Group by primary contributory cause
cause_counts = df['prim_contributory_cause'].value_counts().head(10)  # Top 10 causes

# Create different colors
colors = plt.cm.tab20(np.linspace(0, 1, len(cause_counts)))

# Plotting
plt.figure(figsize=(14,7))
bars = plt.bar(cause_counts.index, cause_counts.values, color=colors)

plt.title('Top 10 Causes of Traffic Accidents')
plt.xlabel('Primary Contributory Cause')
plt.ylabel('Number of Crashes')
plt.xticks(rotation=45, ha='right')
plt.grid(axis='y')
plt.tight_layout()
plt.show()